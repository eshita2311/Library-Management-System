private void loadBooks() {
    try {
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        model.setRowCount(0); // clear table

        ResultSet rs = LibrarianDAO.getAvailableBooks();

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getInt("id"),
                rs.getString("title"),
                rs.getString("author")
            });
        }
    } catch (Exception e) {
        e.printStackTrace();
    }
}


